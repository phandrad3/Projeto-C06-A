public class Usuario {
    int idUsuario;
    String nome;
    String dataNasc;
    String email;
    String senha;


    public Usuario(int idUsuario, String nome, String dataNasc, String email, String senha) {
        this.idUsuario = idUsuario;
        this.nome = nome;
        this.dataNasc = dataNasc;
        this.email = email;
        this.senha = senha;
    }


}
